package ClasseCaixa;

public class Caixa {
    public double saldo;
    public double crédito;
    public double débito;

    public Caixa() {
        this.saldo = saldo;
        this.crédito = crédito;
        this.débito = débito;
    }

    //Métodos:
    public double credito(){

        return saldo += crédito;
    }

    public double debito(){

        return saldo -= débito;
    }
}
